CREATE TABLE region (
	region_id integer PRIMARY KEY,
	region_name text );

CREATE TABLE clime (
	clime_id integer PRIMARY KEY,
	clime_name text);

CREATE TABLE gas (
	gas_id integer PRIMARY KEY,
	gas_name text);

CREATE TABLE brand (
	brand_id integer PRIMARY KEY,
	brand_name text);

CREATE TABLE model (
	model_id integer PRIMARY KEY,
	model_name text);

CREATE TABLE category (
	category_id integer PRIMARY KEY,
	category_name text);
	
CREATE TABLE car_type (
	car_type_id integer PRIMARY KEY,
	car_type_name text);

CREATE TABLE environmental (
	environmental_id integer PRIMARY KEY,
	environmental_name text);

CREATE TABLE drive (
	drive_id integer PRIMARY KEY,
	drive_name text);

CREATE TABLE catalogs (
	catalog_url text PRIMARY KEY,
	category_id integer REFERENCES category (category_id),
	start_production date,
	end_production date,
	msrp integer,
	car_type_id integer REFERENCES car_type (car_type_id),
	doorsnumber integer,
	person_capacity integer,
	weight integer,
	fuel_tank integer,
	boot_capacity integer,
	fuel text,
	environmental_id integer REFERENCES environmental (environmental_id),
	cylinder_layout text,
	cylinders integer,
	drive_id integer REFERENCES drive (drive_id),
	ccm integer,
	consump_city numeric,
	consump_highway numeric,
	consump_mixed numeric,
	top_speed integer,
	acceleration numeric,
	torque integer,
	power integer);
	

CREATE TABLE advertisements (
	ad_id integer PRIMARY KEY,
	region_id integer REFERENCES region (region_id),
	ad_price integer,
	numpictures integer,
	proseller boolean,
	adoldness integer,
	postalcode integer,
	production date,
	mileage integer,
	clime_id integer REFERENCES clime (clime_id),
	gas_id integer REFERENCES gas (gas_id),
	shifter text,
	person_capacity integer,
	doorsnumber integer,
	documentvalid date,
	color integer,
	brand_id integer REFERENCES brand (brand_id),
	model_id integer REFERENCES model (model_id),
	ccm integer,
	highlighted boolean,
	upload_date date,
	description text,
	advertisement_url text,
	catalog_url text REFERENCES catalogs (catalog_url),
	sales_date date,
	is_sold boolean,
	download_date date,
	sales_update_date date);
	

	
	
	
